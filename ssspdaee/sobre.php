            <h5><strong> Qual a origem da sala de situação?</strong> </h5>
            <p style="text-align: justify;">
              A primeira tentativa de criar uma rede de alerta a inundações, em São Paulo, foi feita em fevereiro de 1976, como consequência de uma chuva excepcional, em fins de janeiro, que quase causou o transbordamento do Reservatório de Guarapiranga (o que não ocorreu devido à pronta intervenção do DAEE, que enviou ao local consultores, funcionários e caminhões com sacos de areia para altear e proteger a crista do reservatório). Os dados pluviométricos medidos em algumas estações do DAEE e da EMAE eram transmitidos, por meio de ligação telefônica, a um engenheiro do CTH, Dr. Sérgio Roberto Cirne de Toledo, que os organizava em uma tabela e os levava, pessoalmente, para o gabinete do Superintendente. (vide entrevista do Dr. Sergio em outra matéria deste blog). 
            </p>
            <p>No fim da década de 1970, através de convênio entre DAEE e FDTE – Fundação para o Desenvolvimento Tecnológico de Engenharia da Escola Politécnica da USP foram implantadas as primeiras estações automáticas telemétricas com transmissão dos dados através de linhas telefônicas privativas. A Rede Telemétrica começou com cinco postos e foi se expandindo até atingir a configuração que temos hoje, com aproximadamente 200 postos, e estudos para a implementação de mais 40. </p>

            <p>Em 1988 foi iniciada a operação do Radar Meteorológico do DAEE, que possibilita o monitoramento das precipitações o os seus deslocamentos espaciais, o que muito nos ajudou na interpretação das informações, pois agora além de acompanhar a precipitação nos postos pluviométricos e a variação dos níveis dos rios, observando a continuidade das chuvas que se aproximam, passamos a observar melhor o comportamento dos mesmos. </p>

            <p>Na década de 1990, houve modernizações e o início do uso da Rede Mundial de Computadores (INTERNET). Nesta etapa o Sistema ficou conhecido como SAISP – Sistema de Alerta a Inundações de São Paulo. (vide matéria neste blog sobre este serviço). </p>

            <p>Em 13 de Outubro de 2010, foi implementada a utilização da plataforma Google Earth como base de dados do Radar e Redes Telemétricas, surgindo então a Sala de Situação. Além da Sala de Situação de São Paulo, foram implantadas as salas de Piracicaba, Taubaté e Registro.</p>

            <h5><strong>Qual a finalidade da Sala de Situação:</strong></h5>

            <p>As finalidades da Sala de Situação são apresentar, de forma simples e de fácil compreensão, os dados e informações hidrológicas relacionados a eventos críticos de inundações, tornar mais eficiente o processo de divulgação das informações hidrológicas para o público e a mídia em geral; bem como fornecer subsídios fundamentais para o planejamento de ações governamentais e para o processo de tomada de decisões.</p>

            <h5><strong>Que tipo de dados vocês monitoram?</strong> </h5>

            <p>Os dados monitorados são, basicamente, dados de chuva e de níveis de rios e reservatórios das Bacias do Alto Tietê, Paraíba do Sul, Piracicaba e Ribeira de Iguape. São monitorados também, os níveis de Piscinões e os pontos de inundações mais frequentes, além de esquemas mostrando as informações das estruturas hidráulicas, níveis, vazões e bombeamentos nos principais sistemas de controle de inundações. Estão em fase de desenvolvimento as previsões de níveis de extravasamento em alguns rios da Região Metropolitana de São Paulo – RMSP (atualmente estas previsões são feitas em quatro estações, Ponte do Limão, Ponte do Piqueri, Anhembi e Ponte Dutra).</p>

            <h5><strong>E o cidadão como é beneficiado pela Sala de Situação?</strong></h5>

            <p>A informação com relativa antecedência da aproximação de chuvas com potencial de ocasionar algum tipo de efeito danoso a uma determinada região é transmitida via SMS para diferentes tipos de público, de administradores e técnicos em geral, Defesas Civis e imprensa, até moradores de beiras de cursos d’água que, cientes da possibilidade de ocorrência destes eventos possam tomar providências que venham a minimizar eventuais perdas.</p>

            <h5><strong>O que este trabalho representa para o DAEE?</strong></h5>

            <p>A sistematização da coleta e armazenamento das informações permite a formação de um banco de dados que servirá de base para elaboração de modelos matemáticos de previsão de enchentes para os pontos mais relevantes da Bacia do Alto Tietê, tais como Rios Tietê, Tamanduateí, Aricanduva, Pirajuçara e outros. Sem dúvida, isto contribuirá para reforçar a imagem institucional do DAEE, enquanto agente de prestação de serviços à comunidade. Além da divulgação do nome DAEE quando da emissão dos avisos de alerta.</p>
