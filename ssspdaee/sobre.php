  <h5><strong>Que equipamentos vocês usam?</strong></h5>

 <p>Além dos equipamentos de recepção de dados, temos na SSSP, dois micro computadores com telas dual e dois micros computadores que além de telas dual operam mais cinco monitores, um de 52” e quatro de 32”. A fonte de energia é protegida por nobreak e o sistema de comunicação utiliza a internet empresarial, mais duas outras saídas (speedy e G3).</p>

  <h5><strong>Perspectivas futuras da Sala de Situação:</strong></h5>

 <p>Acreditamos que, nos próximos dois anos, será possível viabilizar a divulgação de informações técnicas para a população e para os meios de comunicação, com a frequência e antecedência adequados sobre a possibilidade de ocorrências de inundações e ações emergenciais.</p>

  <h5><strong>Inaugurada Quarta Sala de Situação em Taubaté:</strong></h5>

 <p>A quarta Sala de Situação do DAEE já está funcionando na regional de Taubaté, no Vale do Paraíba. Com a presença do Superintendente do Departamento de Águas e Energia Elétrica, Alceu Segamarchi, a instalação foi inaugurada na segunda-feira, 30/5, durante a posse do novo diretor da Bacia do Vale do Paraíba, Serra da Mantiqueira e Litoral Norte, o Eng. Nazareno Mostarda Neto.“Trata-se de uma data importante, que nos entusiasma especialmente neste momento em que o Governo do Estado reinicia os trabalhos de desassoreamento do rio Tietê, demostrando como uma de suas prioridades, os investimentos em saneamento e nos recursos hídricos”, lembrou Segamarchi, na cerimônia que contemplou os dois eventos. O Superintendente do DAEE também fez referência especial à inauguração da Sala de Situação de Taubaté, ressaltando que ela reforça a atenção do DAEE em relação ao uso das novas tecnologias.</p>

 <p>A mais recente unidade integra o Sistema de Previsão e Alerta de Enchentes de São Paulo e faz o monitoramento em tempo real das chuvas que caem nos rios da bacia do rio Paraíba do Sul e Litoral Norte, e se necessário, acionam os sistemas de Defesa Civil do estado e das prefeituras da região. O mesmo serviço já existe na sede do DAEE, na capital, nos escritórios da autarquia em Piracicaba, em Registro (Vale do Ribeira) e agora em Taubaté (Vale do Paraíba).</p>

